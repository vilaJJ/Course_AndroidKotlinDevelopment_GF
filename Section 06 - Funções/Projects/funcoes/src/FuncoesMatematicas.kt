import kotlin.math.*

fun main() {
    val maiorValor = max(19, 21)
    val menorValor = min(15, 10)
    val raizQuadrada = sqrt(81f)
    val numeroPI = PI
    val numeroE = E
    val numeroArredondado = round(15.698)

    println("Maior valor: $maiorValor.")
    println("Menor valor: $menorValor.")
    println("Raiz quadrada de 81: $raizQuadrada.")
    println("Valor de PI: $numeroPI.")
    println("Valor da constante de Euler: $numeroE.")
    println("Valor '15.698' arredondado: $numeroArredondado")
}